// Array creation

let cars = ["Thar", "BMW", "Scorpio"];
// console.log(cars);

// Array methods
// Push method
cars.push("Toyota");
cars.push("Ferrari");
console.log("Push",cars);

// Pop method
cars.pop();
console.log("Pop",cars);
console.log('-----------------------------------------');


// Unshift method
cars.unshift("Ferrari")
console.log("Unshift",cars);


// Shift method
cars.shift();
console.log("Shift",cars);